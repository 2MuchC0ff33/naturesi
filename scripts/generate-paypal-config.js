#!/usr/bin/env node
// Generates assets/js/modules/paypal-config.js from .env file (simple parser, no external deps)
import fs from 'node:fs';
import path from 'node:path';

const envPath = path.resolve(process.cwd(), '.env');
let env = {};
if (fs.existsSync(envPath)) {
  const content = fs.readFileSync(envPath, 'utf8');
  for (const line of content.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eq = trimmed.indexOf('=');
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq);
    let val = trimmed.slice(eq + 1);
    // remove surrounding quotes
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
      val = val.slice(1, -1);
    }
    env[key] = val;
  }
} else {
  console.warn('.env file not found; using empty defaults for PayPal config');
}

const paypalBusiness =
  env.PAYPAL_BUSINESS_EMAIL || env.PAYPAL_EMAIL || env.PAYPAL_SANDBOX_MERCHANT_EMAIL || '';
const paypalEnv = (env.PAYPAL_ENV || env.PAYPAL_MODE || 'sandbox').toLowerCase();

const out = `// Auto-generated by scripts/generate-paypal-config.js - DO NOT EDIT\nexport default {
  business: ${JSON.stringify(paypalBusiness)},
  env: ${JSON.stringify(paypalEnv)}
};\n`;

const outPath = path.resolve(process.cwd(), 'assets/js/modules/paypal-config.js');
fs.writeFileSync(outPath, out, 'utf8');
console.log(
  `Wrote PayPal config to ${outPath} (business: ${paypalBusiness || '<empty>'}, env: ${paypalEnv})`
);
